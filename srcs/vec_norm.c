#include "rt_v1.h"

void	vec_norm(t_vec *v)
{
	double	l;

	l = sqrt(pow(v->x, 2) + pow(v->y, 2) + pow(v->z, 2));
	v->x = v->x / l;
	v->y = v->y / l;
	v->z = v->z / l;
}
