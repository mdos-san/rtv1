#include "rt_v1.h"

void	rt_v1_exit(t_env **env, char *str)
{
	if (*env)
	{
		obj_destroy(&(*env)->obj);
		free(*env);
		*env = NULL;
	}
	ft_putendl(str);
	exit(0);
}
